package com.acomminos.morlunk;

public class MorlunkBlogPost {
	
	private String postTitle;
	private String postDescription;
	private String postBody;
	/**
	 * @return the postTitle
	 */
	public String getPostTitle() {
		return postTitle;
	}
	/**
	 * @param postTitle the postTitle to set
	 */
	public void setPostTitle(String postTitle) {
		this.postTitle = postTitle;
	}
	/**
	 * @return the postDescription
	 */
	public String getPostDescription() {
		return postDescription;
	}
	/**
	 * @param postDescription the postDescription to set
	 */
	public void setPostDescription(String postDescription) {
		this.postDescription = postDescription;
	}
	/**
	 * @return the postBody
	 */
	public String getPostBody() {
		return postBody;
	}
	/**
	 * @param postBody the postBody to set
	 */
	public void setPostBody(String postBody) {
		this.postBody = postBody;
	}

}
